#include <iostream>
using namespace std;

int main() {
    int a, b, sum, product;

    cout << "Enter value of a: ";
    cin >> a;

    cout << "Enter value of b: ";
    cin >> b;

    sum = a + b;
    product = a * b;

    cout << "Sum = " << sum << endl;
    cout << "Product = " << product << endl;

    return 0;
}

